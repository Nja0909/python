# Program to create a dictionary with 6 key-value pairs

# Creating a dictionary of a car
car_info = {
    "brand": "Toyota",
    "model": "Innova",
    "year": 2022,
    "color": "White",
    "fuel_type": "Diesel",
    "seating_capacity": 7
}

# Display the dictionary
print("Car Information:")
print(car_info)

# Program to access values using keys

# Using the dictionary from the previous example
print("Brand:", car_info["brand"])
print("Model:", car_info["model"])
print("Fuel Type:", car_info["fuel_type"])
print("Seating Capacity:", car_info["seating_capacity"])
