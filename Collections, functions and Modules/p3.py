# Creating a list with multiple data types
my_list = [25, "Hello", 3.14, True, None, [1, 2, 3]]

# Printing the list
print("List with multiple data types:", my_list)

# Printing the type of each element
print("Data types of elements:")
for element in my_list:
    print(f"{element} --> {type(element)}")

