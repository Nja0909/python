# Importing the math module
import math

# Sample number
number = 7.8

# Using math functions
square_root = math.sqrt(number)
ceil_value = math.ceil(number)
floor_value = math.floor(number)

# Displaying results
print("Original number:", number)
print("Square root:", square_root)
print("Ceiling value:", ceil_value)
print("Floor value:", floor_value)

# Importing the random module
import random

# Generating random numbers
rand_int = random.randint(1, 100)
rand_float = random.random()
rand_choice = random.choice(["apple", "banana", "cherry"])

# Displaying results
print("Random integer between 1 and 100:", rand_int)
print("Random float between 0 and 1:", rand_float)
print("Random fruit selected:", rand_choice)
