# Predefined list
colors = ["Red", "Blue", "Green", "Yellow", "Purple"]

# Iterating through the list and printing each element
print("Colors in the list:")
for color in colors:
    print(color)


# Creating an empty list
numbers = []

# Using a for loop to insert numbers into the list
for i in range(1, 6):  # Numbers from 1 to 5
    numbers.append(i)

# Printing the final list
print("List after inserting elements:", numbers)
