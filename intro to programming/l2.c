#include <stdio.h>

int main() {
    // Declaring variables
    int age = 20;
    float height = 5.8;
    char grade = 'A';

    // Declaring a constant
    const float PI = 3.14159;

    // Displaying the values
    printf("Age: %d\n", age);
    printf("Height: %.1f\n", height);
    printf("Grade: %c\n", grade);
    printf("Value of PI (constant): %.5f\n", PI);

    return 0; // Program ends here
}
